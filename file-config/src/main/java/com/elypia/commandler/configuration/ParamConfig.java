package com.elypia.commandler.configuration;

public class ParamConfig {

    private String name;
    private String help;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHelp() {
        return help;
    }

    public void setHelp(String help) {
        this.help = help;
    }
}
